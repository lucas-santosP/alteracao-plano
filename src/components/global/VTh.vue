<template>
  <th
    :style="{
      textAlign: align,
      width: thWidth,
      maxWidth: thWidth,
      padding: thPadding,
      verticalAlign: 'middle',
    }"
  >
    <slot></slot>
  </th>
</template>

<script>
export default {
  name: "v-th",
  props: {
    width: { type: [String, Number], default: "" },
    align: { type: String, default: "center" },
    paddingX: { type: [String, Number], default: "5" },
  },

  computed: {
    thWidth() {
      return `${this.width}px`;
    },
    thPadding() {
      return `0 ${this.paddingX}px`;
    },
  },
};
</script>
